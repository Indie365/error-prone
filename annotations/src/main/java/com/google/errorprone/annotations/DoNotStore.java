package com.google.errorprone.annotations;

import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.RetentionPolicy.CLASS;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * The method to which this annotation is applied should not have its return value assigned to a
 * variable, but should instead be used directly as another method parameter.
 */
@Retention(CLASS)
@Target(METHOD)
public @interface DoNotStore {
  /** An optional explanation of why the returned value should not be stored. */
  String value() default "";
}
